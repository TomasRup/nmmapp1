package eu.rupsys.plot.function;

public class PlotPoint {

    private final double x;
    private final double y;

    public PlotPoint(final double x, final double y) {
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
